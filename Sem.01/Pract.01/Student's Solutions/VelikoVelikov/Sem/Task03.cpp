
#include <iostream>

using namespace std;

int main()
{
	char sym; cin >> sym;
	switch (sym)
	{
	case 'a': cout << 1; break;
	case 'e': cout << 1; break;
	case 'i': cout << 1; break;
	case 'u': cout << 1; break;
	case 'o': cout << 1; break;
	case 'A': cout << 1; break;
	case 'E': cout << 1; break;
	case 'I': cout << 1; break;
	case 'U': cout << 1; break;
	case 'O': cout << 1; break;
	default: cout << 0; break;
	}
}
